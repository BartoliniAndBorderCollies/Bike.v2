package com.klodnicki.Bike.v2.model;

/**
 * Represents the type of bike.
 * This is an enumeration with two possible values: TRADITIONAL and ELECTRIC.
 */
public enum BikeType {

    /**
     * Represents a traditional bike.
     * Traditional bikes are human-powered, meaning they require pedaling to move.
     */
    TRADITIONAL,
    /**
     * Represents an electric bike.
     * Electric bikes are equipped with an electric motor that can be used to assist propulsion.
     */
    ELECTRIC
}
